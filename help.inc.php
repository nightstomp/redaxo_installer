		<p class="rex-tx1">Genervt vom ständigen Upload von Addons?<br />
			Immer wieder Redaxo.org aufrufen, Addon downloaden, entpacken und auf den eigenen Server hochladen kann lästig sein. Dieses Addon nimmt Dir die komplette Arbeit ab. Selbst ein Besuch auf Redaxo.org ist dank der Suche überflüssig. Alle verfügbare Addons werden unter "Addon suchen" angezeigt und können mit zwei Klicks automatisch installiert werden.
		</p>
		
